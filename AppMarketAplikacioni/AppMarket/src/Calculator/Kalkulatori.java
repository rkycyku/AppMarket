    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        int n1 = Integer.parseInt(a.getText());
        int n2 = Integer.parseInt(b.getText());

        String n1s = String.valueOf(n1);
        String n2s = String.valueOf(n2);

        if(n1s.length() > 0 && n2 == 0){
            double rn1 = Math.sqrt(n1);
            c.setText("" + rn1);
        }
        else if(n2s.length() > 0 && n1 == 0 ){
            double rn2 = Math.sqrt(n2);
            c.setText("" + rn2);
        }
        else{
            a.setText("0");
            b.setText("0");
            c.setText("Ju lutem shenoni vetem nje numer!");
        }
    }//GEN-LAST:event_jButton7ActionPerformed

